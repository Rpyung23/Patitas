package com.franciscorivera.patitas.poo;

public class cDuenio
{
    private String nameDuenio;
    private String emailDuenio;
    private String phoneDuenio;
    private String dirDuenio;

    public cDuenio() {
    }

    public String getNameDuenio() {
        return nameDuenio;
    }

    public void setNameDuenio(String nameDuenio) {
        this.nameDuenio = nameDuenio;
    }

    public String getEmailDuenio() {
        return emailDuenio;
    }

    public void setEmailDuenio(String emailDuenio) {
        this.emailDuenio = emailDuenio;
    }

    public String getPhoneDuenio() {
        return phoneDuenio;
    }

    public void setPhoneDuenio(String phoneDuenio) {
        this.phoneDuenio = phoneDuenio;
    }

    public String getDirDuenio() {
        return dirDuenio;
    }

    public void setDirDuenio(String dirDuenio) {
        this.dirDuenio = dirDuenio;
    }
}
